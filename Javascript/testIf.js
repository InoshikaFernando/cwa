/**
 * Create a function call "CheckValue" so that
 * you can call the function as CheckValue(5)
 * if the value you pass is greater than 10, print "Greater than 10"
 * else, print "Less than or equal to 10"
 */
function CheckValue(Value) {
    if (Value > 10) {
        console.log('Greater than 10');
    } else {
        console.log('Less than or equal to 10');
    }
}